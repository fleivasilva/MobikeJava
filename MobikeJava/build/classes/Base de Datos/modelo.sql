CREATE TABLE Cliente (
    ID_CLIENTE int NOT NULL,
    RUT VARCHAR(40),
    NOMBRE_COMPLETO VARCHAR(40),
    EDAD int,
    MEDIO_PAGO ENUM('Debito', 'Credito', 'Tarjeta Municipal'),
    NUMERO_CELULAR VARCHAR(40),
    EMAIL VARCHAR(40),
    PRIMARY KEY (ID_CLIENTE)
) ENGINE=INNODB;

CREATE TABLE BICICLETA(
    ID_BICICLETA int NOT NULL,
    LATITUD VARCHAR(40),
    LONGITUD VARCHAR(40),
    CODIGO_MOBIKE VARCHAR(40),
    ESTADO_CANDADO ENUM('Bloqueado', 'Libre'),
    PRIMARY KEY (ID_BICICLETA)
) ENGINE=INNODB;

CREATE TABLE Arriendo (
  ID_ARRIENDO int NOT NULL,
  CODIGO_ARRIENDO VARCHAR(40),
  HORA_REGISTRO DATETIME,
  CLIENTE INT,
  BICICLETA INT,
    INDEX Bicicleta_ind (BICICLETA),
    INDEX cliente_ind (CLIENTE),
    PRIMARY KEY (ID_ARRIENDO),
    FOREIGN KEY (CLIENTE)
        REFERENCES Cliente(ID_CLIENTE)
        ON DELETE CASCADE,
    FOREIGN KEY (BICICLETA)
        REFERENCES Bicicleta(ID_BICICLETA)
        ON DELETE CASCADE
) ENGINE=INNODB;
